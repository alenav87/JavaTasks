public class CalcOperationMethods {

    double n1, n2;

    public double Sum (double n1, double n2){
        double sum = n1 + n2;
        return sum;

    }

    public double Del (double n1, double n2){
        double del = n1 - n2;
        return del;

    }

    public double Div (double n1, double n2){
        double div = n1 / n2;
        return div;

    }

    public double Mult (double n1, double n2){
        double mult = n1 * n2;
        return mult;

    }
}
